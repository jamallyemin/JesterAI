let activeLang = 'en';
let t = popupTranslations.en;

function applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.dataset.i18n;
        if (t[key] !== undefined) el.textContent = t[key];
    });
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
        const key = el.dataset.i18nHtml;
        if (t[key] !== undefined) el.innerHTML = t[key];
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.dataset.i18nPlaceholder;
        if (t[key] !== undefined) el.placeholder = t[key];
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        const key = el.dataset.i18nTitle;
        if (t[key] !== undefined) el.title = t[key];
    });
}

document.addEventListener('DOMContentLoaded', () => {
    const statusEl    = document.getElementById('status');
    const reasonEl    = document.getElementById('reason');
    const waybackEl   = document.getElementById('wayback-verdict');
    const powerSwitch = document.getElementById('powerSwitch');
    const whiteListBtn = document.getElementById('whitelist-btn');
    const optionsTrigger = document.getElementById('options-trigger');
    const settingsPanel  = document.getElementById('settings-panel');
    const apiKeyInput    = document.getElementById('api-key-input');
    const saveKeyBtn     = document.getElementById('save-key-btn');

    chrome.storage.local.get({ jesterLightMode: false, jesterLanguage: 'en' }, (r) => {
        if (r.jesterLightMode) document.body.classList.add('light');
        activeLang = popupTranslations[r.jesterLanguage] ? r.jesterLanguage : 'en';
        t = popupTranslations[activeLang];
        applyTranslations();
    });

    if (optionsTrigger) {
        optionsTrigger.addEventListener('click', () => {
            const isHidden = settingsPanel.style.display === 'none' || settingsPanel.style.display === '';
            settingsPanel.style.display = isHidden ? 'block' : 'none';
            if (isHidden) {
                renderWhitelist();
                chrome.storage.local.get(['gemini_api_key'], (result) => {
                    if (result.gemini_api_key) apiKeyInput.value = atob(result.gemini_api_key);
                });
            }
        });
    }

    if (saveKeyBtn) {
        saveKeyBtn.addEventListener('click', () => {
            const key = apiKeyInput.value.trim();
            if (key) {
                const encodedKey = btoa(key);
                chrome.storage.local.set({ gemini_api_key: encodedKey }, () => {
                    saveKeyBtn.textContent = t.savedConfirm;
                    saveKeyBtn.style.background = 'var(--green)';
                    saveKeyBtn.style.color = '#000';
                    setTimeout(() => {
                        saveKeyBtn.textContent = t.saveConfig;
                        saveKeyBtn.style.background = 'transparent';
                        saveKeyBtn.style.color = 'var(--green)';
                        settingsPanel.style.display = 'none';
                    }, 1000);
                });
            } else {
                alert(t.apiKeyAlert);
            }
        });
    }

    chrome.tabs.query({ active: true, currentWindow: true }, (tabs) => {
        if (!tabs[0]) return;
        const currentTabId = tabs[0].id;
        let currentUrl;
        try { currentUrl = new URL(tabs[0].url).hostname; } catch (e) { return; }

        chrome.storage.local.get(['lastVerdict', 'lastWayback', 'isJesterActivated', 'userWhitelist'], (data) => {
            const isActive = data.isJesterActivated !== false;
            const whitelist = data.userWhitelist || [];

            if (powerSwitch) powerSwitch.checked = isActive;
            updatePowerUI(isActive, statusEl, reasonEl);

            if (whitelist.includes(currentUrl)) {
                updateUI({ status: "SAFE", reason: t.trusted.replace(' ✓', '') + " (Jester Whitelist)." }, statusEl, reasonEl);
                if (whiteListBtn) { whiteListBtn.textContent = t.trusted; whiteListBtn.classList.add('trusted'); }
                return;
            }

            const isDataValid = data.lastVerdict &&
                data.lastVerdict.tabId === currentTabId &&
                data.lastVerdict.hostname === currentUrl;

            if (isActive && isDataValid) {
                updateUI(data.lastVerdict, statusEl, reasonEl);
                if (data.lastWayback && data.lastWayback.hostname === currentUrl) {
                    renderWayback(data.lastWayback, waybackEl);
                }
            } else if (isActive) {
                if (statusEl) statusEl.textContent = t.analyzing;
                if (reasonEl) reasonEl.textContent = t.scanning;
                if (whiteListBtn) whiteListBtn.textContent = t.trustSite;
                setPulseDot('analyzing');
            }
        });
    });

    chrome.runtime.onMessage.addListener((message, sender, sendResponse) => {
        if (message.action === "updatepopup" && statusEl && reasonEl) {
            chrome.storage.local.get(['isJesterActivated'], (result) => {
                const activeNow = result.isJesterActivated !== false;
                if (activeNow) updateUI(message, statusEl, reasonEl);
            });
            sendResponse({ result: "success" });
        }
        if (message.action === "updateWayback" && waybackEl) {
            renderWayback(message, waybackEl);
        }
    });

    if (whiteListBtn) {
        whiteListBtn.addEventListener('click', () => {
            if (whiteListBtn.classList.contains('trusted')) return;
            chrome.tabs.query({ active: true, currentWindow: true }, (tabs) => {
                if (!tabs[0]) return;
                let url;
                try { url = new URL(tabs[0].url).hostname; } catch (e) { return; }
                chrome.storage.local.get({ userWhitelist: [] }, (data) => {
                    let list = data.userWhitelist;
                    if (!list.includes(url)) {
                        list.push(url);
                        chrome.storage.local.set({ userWhitelist: list }, () => {
                            whiteListBtn.textContent = t.trusted;
                            whiteListBtn.classList.add('trusted');
                            chrome.tabs.reload(tabs[0].id);
                        });
                    }
                });
            });
        });
    }

    const refreshBtn = document.getElementById("refreshBtn");
    if (refreshBtn) {
        refreshBtn.addEventListener("click", () => {
            chrome.tabs.query({ active: true, currentWindow: true }, (tabs) => {
                if (tabs[0]?.id) chrome.tabs.reload(tabs[0].id);
            });
        });
    }

    if (powerSwitch) {
        powerSwitch.addEventListener('change', () => {
            const newState = powerSwitch.checked;
            updatePowerUI(newState, statusEl, reasonEl);
            if (!newState) chrome.storage.local.remove(['lastVerdict', 'lastWayback']);
            chrome.storage.local.set({ isJesterActivated: newState }, () => {
                chrome.tabs.query({ active: true, currentWindow: true }, (tabs) => {
                    if (tabs[0]?.id) chrome.tabs.reload(tabs[0].id);
                });
            });
        });
    }

    const lightToggle = document.getElementById('setting-lightmode');
    if (lightToggle) {
        chrome.storage.local.get({ jesterLightMode: false }, r => {
            lightToggle.checked = r.jesterLightMode;
        });
        lightToggle.addEventListener('change', () => {
            document.body.classList.toggle('light', lightToggle.checked);
            chrome.storage.local.set({ jesterLightMode: lightToggle.checked });
        });
    }

    const ratingToggle = document.getElementById('setting-showrating');
    if (ratingToggle) {
        chrome.storage.local.get({ jesterShowRating: true }, r => {
            ratingToggle.checked = r.jesterShowRating;
        });
        ratingToggle.addEventListener('change', () => {
            chrome.storage.local.set({ jesterShowRating: ratingToggle.checked });
        });
    }

    const notifToggle = document.getElementById('setting-notifications');
    if (notifToggle) {
        chrome.storage.local.get({ jesterNotifications: true }, r => {
            notifToggle.checked = r.jesterNotifications;
        });
        notifToggle.addEventListener('change', () => {
            chrome.storage.local.set({ jesterNotifications: notifToggle.checked });
        });
    }

    const segBtns = document.querySelectorAll('.seg-btn');
    if (segBtns.length) {
        chrome.storage.local.get({ jesterSensitivity: 'medium' }, r => {
            segBtns.forEach(b => b.classList.toggle('active', b.dataset.val === r.jesterSensitivity));
        });
        segBtns.forEach(btn => {
            btn.addEventListener('click', () => {
                segBtns.forEach(b => b.classList.remove('active'));
                btn.classList.add('active');
                chrome.storage.local.set({ jesterSensitivity: btn.dataset.val });
            });
        });
    }

    const langBtns = document.querySelectorAll('.lang-btn');
    if (langBtns.length) {
        chrome.storage.local.get({ jesterLanguage: 'en' }, r => {
            langBtns.forEach(b => b.classList.toggle('active', b.dataset.lang === r.jesterLanguage));
        });
        langBtns.forEach(btn => {
            btn.addEventListener('click', () => {
                langBtns.forEach(b => b.classList.remove('active'));
                btn.classList.add('active');
                const chosen = btn.dataset.lang;
                chrome.storage.local.set({ jesterLanguage: chosen }, () => {
                    activeLang = popupTranslations[chosen] ? chosen : 'en';
                    t = popupTranslations[activeLang];
                    applyTranslations();
                });
            });
        });
    }

    document.querySelectorAll('.nav-btn').forEach(btn => {
        btn.addEventListener('click', () => {
            document.querySelectorAll('.nav-btn').forEach(b => b.classList.remove('active'));
            document.querySelectorAll('.page').forEach(p => p.classList.remove('active'));
            btn.classList.add('active');
            document.getElementById('page-' + btn.dataset.page).classList.add('active');
        });
    });
});

function updateUI(data, statusEl, reasonEl) {
    if (!statusEl || !reasonEl) return;
    chrome.storage.local.get({ jesterShowRating: true }, (r) => {
        statusEl.textContent = data.status + (r.jesterShowRating && data.rating ? ` (${data.rating}/10)` : '');
        reasonEl.textContent = data.reason || '';
        statusEl.classList.remove('status-danger', 'status-safe', 'status-warn');
        if (data.status === "DANGER") {
            statusEl.classList.add('status-danger');
            setPulseDot('danger');
        } else if (data.status === "SAFE") {
            statusEl.classList.add('status-safe');
            setPulseDot('safe');
        } else {
            setPulseDot('analyzing');
        }
    });
}

function updatePowerUI(isActive, statusEl, reasonEl) {
    if (!statusEl) return;
    if (isActive) {
        statusEl.textContent = t.analyzing;
        statusEl.className = '';
        setPulseDot('analyzing');
        if (reasonEl && reasonEl.textContent === t.paused) {
            reasonEl.textContent = t.waitingGemini;
        }
    } else {
        statusEl.textContent = t.standby;
        statusEl.className = 'status-standby';
        setPulseDot('standby');
        if (reasonEl) reasonEl.textContent = t.paused;
    }
}

function setPulseDot(state) {
    const dot = document.getElementById('pulse-dot');
    if (!dot) return;
    dot.className = 'pulse-dot';
    if (state === 'danger')  dot.classList.add('danger');
    if (state === 'standby') dot.classList.add('standby');
}

function renderWayback(data, el) {
    if (!el) return;
    el.className = 'wayback-verdict';
    if (data.status === 'never') {
        el.textContent = t.waybackNever;
        el.classList.add('visible', 'warn');
    } else if (data.status === 'new') {
        el.textContent = t.waybackNew.replace('{days}', data.days);
        el.classList.add('visible', 'warn');
    } else if (data.status === 'ok') {
        el.textContent = t.waybackOk.replace('{date}', data.firstSeen).replace('{count}', data.count);
        el.classList.add('visible', 'ok');
    }
}

function renderWhitelist() {
    const listContainer = document.getElementById('whitelist-items');
    if (!listContainer) return;
    chrome.storage.local.get({ userWhitelist: [] }, (data) => {
        listContainer.innerHTML = '';
        if (data.userWhitelist.length === 0) {
            const empty = document.createElement('p');
            empty.style.cssText = "color:var(--dimmed);font-style:italic;font-size:10px;";
            empty.textContent = t.noDomains;
            listContainer.appendChild(empty);
            return;
        }
        data.userWhitelist.forEach((domain, index) => {
            const item = document.createElement('div');
            item.className = 'wl-item';
            const domainSpan = document.createElement('span');
            domainSpan.className = 'wl-domain';
            domainSpan.textContent = domain || "Local File / No Domain";
            const removeSpan = document.createElement('span');
            removeSpan.className = 'wl-remove';
            removeSpan.textContent = '[X]';
            removeSpan.dataset.index = index;
            item.appendChild(domainSpan);
            item.appendChild(removeSpan);
            listContainer.appendChild(item);
        });
        document.querySelectorAll('.wl-remove').forEach(btn => {
            btn.onclick = (e) => {
                const idx = parseInt(e.target.getAttribute('data-index'));
                chrome.storage.local.get({ userWhitelist: [] }, (res) => {
                    const newList = [...res.userWhitelist];
                    newList.splice(idx, 1);
                    chrome.storage.local.set({ userWhitelist: newList }, renderWhitelist);
                });
            };
        });
    });
}
